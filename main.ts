controller.A.onEvent(ControllerButtonEvent.Pressed, function () {
    if (cat.vy == 0) {
        jump()
    }
})
sprites.onOverlap(SpriteKind.Player, SpriteKind.Enemy, function (sprite, otherSprite) {
    if (sprite.bottom < otherSprite.bottom) {
        info.changeScoreBy(2)
    } else {
        info.changeLifeBy(-1)
    }
    otherSprite.destroy()
})
